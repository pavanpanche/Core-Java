package JavaDemo;

public class JavaVariables {
    public static void main(String[] args) {
        int a = 11;  // variable initialization (datatype varname=value;)


        int b;       // Datatype varname;==========>variable declaration
        b = 10;      // varName=value==============>assigning Value

        System.out.println(a);
        System.out.println(b);
    }
}

