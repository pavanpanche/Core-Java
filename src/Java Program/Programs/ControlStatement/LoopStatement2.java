package JavaDemo.Programs.ControlStatement;

public class LoopStatement2 {
    public static void main(String[] args) {

        //for loop statement factorial problem
        int n=7;
        int factorial=1;
        for(int i=1;i<=n;i++)
        {

            factorial=factorial*i;

        }
        System.out.println(factorial);

    }

}
